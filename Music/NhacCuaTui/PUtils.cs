        internal static string HexFromArray(List<int> array)
        {
            var s = "";
            for (int i = 0; i < array.Count; i++)
            {
                try
                {
                    if (i == 0)
                    {
                        if (utf8char[array[i].ToString()] != '\0')
                            s += utf8char[array[i] + ""];
                    }
                    else
                    {
                        if (array[i] >= 224)
                        {
                            if (utf8char[array[i] + " " + array[i + 1] + " " + array[i + 2]] != '\0')
                                s += utf8char[array[i] + " " + array[i + 1] + " " + array[i + 2]];
                        }
                        else if (array[i] >= 128)
                        {
                            if (utf8char[array[i] + " " + array[i + 1]] != '\0')
                                s += utf8char[array[i] + " " + array[i + 1]];
                        }
                        else
                        {
                            if ((i > 0 && array[i - 1] < 128) || i > 1 && array[i - 2] < 200)
                            {
                                if (utf8char[array[i].ToString()] != '\0')
                                s += utf8char[array[i] + ""];
                            }
                        }
                    }
                }
                catch (Exception) { }
            }
            return s;
        }

        internal static List<int> HexToArray(string str)
        {
            var result = new List<int>();
            while (str.Length >= 2)
            {
                result.Add(int.Parse(str.Substring(0, 2), NumberStyles.HexNumber));
                str = str.Substring(2, str.Length - 2);
            }
            return result;
        }

